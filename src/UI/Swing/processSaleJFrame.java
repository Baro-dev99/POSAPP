    private void PayActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_PayActionPerformed
        MyAdapter adap=null;
        String[] Payment = {"Cash", "CreditCard", "Check"};
        int optionChoosed = JOptionPane.showOptionDialog(null, "Choose your payment method !", "PAYMENT METHOD",
                0, JOptionPane.WARNING_MESSAGE, null, Payment, Payment[1]);
        
        String pay = register.makePayment(adap, Payment, optionChoosed);
        JOptionPane.showMessageDialog(null, pay);
        
        try {
        // save data to the db
        int totalQty = 0;
        for(int i = 0; i < noQtyList.size(); i++)
            totalQty += noQtyList.get(i);
        double totalAmount = totalPrice+ totalPrice*taxPercentage;
        Sale sale = register.makeNewSale(noItems,totalQty, totalAmount, Payment, optionChoosed);
        
        register.getPersistentStorage().createSale(sale);
        } catch (SQLException ex) {
            System.out.println(ex);
        }
        
        // CLEARING AND RESETTING ALL
        
        // buttons
        Pay.setEnabled(false);
        RemoveItem.setEnabled(false);
        EndSale.setEnabled(false);
        AddItem.setEnabled(false);
        
        // global vars
        noItems = 0;
        noQtyList.clear();
        totalPrice = 0;
        
        // text fields and labels
        NoItems.setText("NoItems");
        NoQty.setText("NoQty");
        ProdQty.setText(0 + "");
        Price.setText("Price");
        TotalPrice.setText("TotalPrice");
        TotalTax.setText("TotalTax");
        TotalAmount.setText("TotalAmount");
        
        // jLists and arrayLists
        YourItemsModel.clear();
        jListYourItems.setModel(YourItemsModel);
        YourProductList.clear();
        
        // tabbed panes
        this.jTabbedPane2.setSelectedIndex(0);
        this.jTabbedPane2.setVisible(false);
    }//GEN-LAST:event_PayActionPerformed

