    private void AddItemActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_AddItemActionPerformed

        // filling the customer bag
        double prodPrice = currentProduct.getPrice();
        int prodQty = Integer.parseInt(ProdQty.getText());
        if (prodQty > 0) {
            EndSale.setEnabled(true);
            RemoveItem.setEnabled(true);
            YourItemsModel.addElement(currentProduct.getDescription()
                    + " (" + ProdQty.getText() + ")  -  "
                    + prodPrice * prodQty + " $");
            jListYourItems.setModel(YourItemsModel);
            totalPrice += prodPrice * prodQty;
            
            YourProductList.add(currentProduct);
        }
        
        // set the number of items
        noItems++;
        NoItems.setText(String.valueOf(noItems));
        
        // set the number of Quantity
        noQtyList.add(Integer.parseInt(ProdQty.getText()));
        int totalQty = 0;
        for(int i = 0; i < noQtyList.size(); i++)
            totalQty += noQtyList.get(i);
        NoQty.setText(totalQty + "");
    }//GEN-LAST:event_AddItemActionPerformed

    private void RemoveItemActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_RemoveItemActionPerformed
        // In case the customer doesn't want the item anymore
        
        // updating total price
        String itemString = YourItemsModel.get(jListYourItems.getSelectedIndex());
        String[] array = itemString.split(" ");
//        System.out.println(array[5]);
        totalPrice -= Double.valueOf(array[5]);

        // updating number of items
        noItems--;
        NoItems.setText(String.valueOf(noItems));
        
        // updating number of quantity
        noQtyList.remove(jListYourItems.getSelectedIndex());
        int totalQty = 0;
        for(int i = 0; i < noQtyList.size(); i++)
            totalQty += noQtyList.get(i);
        NoQty.setText(totalQty + "");
        
        // removing selected item from jList and arrayList of products
        YourItemsModel.remove(jListYourItems.getSelectedIndex());
        jListYourItems.setModel(YourItemsModel);
        YourProductList.remove(yourProd);

        // disable button if list is empty
        if (YourItemsModel.isEmpty()) {
            RemoveItem.setEnabled(false);
            EndSale.setEnabled(false);
        }
    }//GEN-LAST:event_RemoveItemActionPerformed

